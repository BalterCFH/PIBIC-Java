    private void jMenuItem10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem10ActionPerformed
        // TODO add your handling code here:
        final List<String> codigos = new ArrayList<String>();
        JFileChooser chooser = new JFileChooser();
        int sav = chooser.showSaveDialog(null);
        if (sav == JFileChooser.APPROVE_OPTION) {
            File arq = chooser.getSelectedFile().getAbsoluteFile();
            String dir = arq.getPath();

            codigos.add("C:\\Program Files\\PostgreSQL\\11\\bin\\pg_dump.exe");

            codigos.add("-h");
            codigos.add("localhost");
            codigos.add("-p");
            codigos.add("5432");
            codigos.add("-U");
            codigos.add("postgres");
            codigos.add("-F");
            codigos.add("c");
            codigos.add("-b");
            codigos.add("-v");
            codigos.add("-f");
            codigos.add(dir + ".backup");
            codigos.add("agroindustria");

            ProcessBuilder pb = new ProcessBuilder(codigos);

            pb.environment().put("PGPASSWORD", "123");

            try {
                final Process process = pb.start();

                try (BufferedReader r = new BufferedReader(
                        new InputStreamReader(process.getErrorStream()))) {
                    String line = r.readLine();
                    while (line != null) {
                        System.err.println(line);
                        line = r.readLine();
                    }
                }

                process.waitFor();
                process.destroy();
                JOptionPane.showMessageDialog(null, "O Backup foi um sucesso.");

            } catch (IOException | InterruptedException e) {

                JOptionPane.showMessageDialog(null, "O Backup falhou" + e.getMessage());

            }
        }
    }//GEN-LAST:event_jMenuItem10ActionPerformed

